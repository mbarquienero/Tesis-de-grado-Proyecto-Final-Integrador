var arDroneConstants = require("ar-drone/lib/constants");

var droneConfiguration = function(drone) {

    var navdata_options = arDroneConstants.options.DEMO |
    arDroneConstants.options.VISION_DETECT |
    arDroneConstants.options.MAGNETO |
    arDroneConstants.options.ALTITUDE |
    arDroneConstants.options.WIFI;

    drone.config("general:navdata_demo", true);
    drone.config("general:navdata_options", navdata_options);
};

module.exports = droneConfiguration;